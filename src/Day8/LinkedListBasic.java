package Day8;
import java.util.*;

public class LinkedListBasic {
    public static void main(String[] args) {
        LinkedList<Integer> ll = new LinkedList<>();
        ll.add(90);
        System.out.println(ll);
        ll.addFirst(89);
        System.out.println(ll);
        ll.addLast(890);
        System.out.println(ll);
        ll.offer(10);
        System.out.println(ll);
        ll.offerFirst(45);
        System.out.println(ll);
        ll.get(4);
         System.out.println(ll);
        ll.getFirst();
          System.out.println(ll);
          ll.getLast();
           System.out.println(ll);
           ll.peek();
            System.out.println(ll);
            ll.peekFirst();
             System.out.println(ll);
             ll.peekLast();
              System.out.println(ll);
              ll.remove();
               System.out.println(ll);
               ll.removeFirst();
                System.out.println(ll);
        
         
        
    }
        
    }
    

